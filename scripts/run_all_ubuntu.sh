- Baseline point cloud: \`$OUTPUT_ROOT/baseline/point_cloud/iteration_${ITERATIONS}/point_cloud.ply\`
- Baseline metrics: \`$REPORT_DIR/metrics/baseline/results.json\`
- Baseline per-view metrics: \`$REPORT_DIR/metrics/baseline/per_view.json\`
