- Result directory: \`$RESULT_ROOT\`
- Model runs directory: \`$OUTPUT_ROOT\`
- Input images: \`$(count_input_images)\`
- Baseline iterations: \`$ITERATIONS\`
- Run comparisons: \`$RUN_COMPARISONS\`
